branin <- function(x) {
	x1 <- x[1]*15-5
	x2 <- x[2]*15
	(x2 - 5/(4*pi^2)*(x1^2) + 5/pi*x1 - 6)^2 + 10*(1 - 1/(8*pi))*cos(x1) + 10
}

camelback <- function(x){
	x1 <- x[1]*6-3
	x2 <- x[2]*4-2
	(4-2.1*x1^2+(x1^4)/3)*x1^2+x1*x2+(-4+4*x2^2)*x2^2
}

goldsteinPrice<- function(x){
	x1 <- x[1]*4-2;
